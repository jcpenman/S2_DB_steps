CREATE OR REPLACE TRIGGER CRSE_BEFORE_U
   BEFORE INSERT OR UPDATE
   ON CRSE
   FOR EACH ROW
DECLARE
BEGIN
   :NEW.LAST_UPDATED_ON := SYSDATE;

   IF     :NEW.CRSE_NAME IS NOT NULL
      AND NVL (:NEW.CRSE_NAME, ' ') <> NVL (:OLD.CRSE_NAME, ' ')
   THEN
      :NEW.CRSE_NAME := UPPER (:NEW.CRSE_NAME);
   END IF;
END CRSE_BEFORE_U;