CREATE OR REPLACE TRIGGER CRSE_SESSION_IUD
   AFTER INSERT OR DELETE OR UPDATE OF MAX_DURATION
   ON CRSE_SESSION
   FOR EACH ROW
BEGIN
   INSERT INTO SLCADMIN.RESET_COURSE RC (RC.CRSE_ID, RC.SESSION_CODE)
        SELECT  NVL (:NEW.CRSE_ID, :OLD.CRSE_ID), NVL(:NEW.SESSION_CODE, :OLD.SESSION_CODE)
        FROM DUAL
        WHERE NOT EXISTS (SELECT 1
        FROM  SLCADMIN.RESET_COURSE RC1
        WHERE  RC1.CRSE_ID = NVL (:NEW.CRSE_ID, :OLD.CRSE_ID)
         AND RC1.SESSION_CODE = NVL(:NEW.SESSION_CODE, :OLD.SESSION_CODE)); 
   IF UPDATING AND NVL(:OLD.MAX_DURATION,0) != NVL(:NEW.MAX_DURATION,0)
   THEN
          INSERT INTO crse_term_change
                  (crse_year_id, change_date
                  )
          SELECT CY.CRSE_YEAR_ID, SYSDATE
          FROM CRSE_YEAR CY
          WHERE CY.CRSE_SESSION_ID = :NEW.CRSE_SESSION_ID;

   END IF;
END;
