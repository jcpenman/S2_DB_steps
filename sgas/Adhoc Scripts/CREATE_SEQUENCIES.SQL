DECLARE

    V_MAX_CAMPUS_ID NUMBER;
    V_MAX_CRSE_ID NUMBER;
    V_MAX_CRSE_SESSION_ID NUMBER;
    V_MAX_CRSE_YEAR_ID NUMBER;
    V_CAMPUS_SEQ_STR VARCHAR2(1000)         := 'CREATE SEQUENCE CAM_CAMPUS_ID_SEQ START WITH ';
    V_CRSE_SEQ_STR VARCHAR2(1000)           := 'CREATE SEQUENCE CRS_CRSE_ID_SEQ START WITH ';
    V_CRSE_SESSION_SEQ_STR VARCHAR2(1000)   := 'CREATE SEQUENCE CRSS_CRSE_SESSION_ID_SEQ START WITH ';
    V_CRSE_YEAR_SEQ_STR VARCHAR2(1000)      := 'CREATE SEQUENCE CRSY_CRSE_YEAR_ID_SEQ START WITH ';
    V_SEQ1 VARCHAR2(1000) := ' INCREMENT BY 1 MAXVALUE 999999999 NOCACHE';

BEGIN

    SELECT MAX(CAMPUS_ID) + 1 INTO V_MAX_CAMPUS_ID FROM CAMPUS;
    SELECT MAX(CRSE_ID) + 1 INTO V_MAX_CRSE_ID FROM CRSE;
    SELECT MAX(CRSE_SESSION_ID) + 1 INTO V_MAX_CRSE_SESSION_ID FROM CRSE_SESSION;
    SELECT MAX(CRSE_YEAR_ID) + 1 INTO V_MAX_CRSE_YEAR_ID FROM CRSE_YEAR;

    EXECUTE IMMEDIATE V_CAMPUS_SEQ_STR||TO_CHAR(V_MAX_CAMPUS_ID)||V_SEQ1;
    EXECUTE IMMEDIATE V_CRSE_SEQ_STR||TO_CHAR(V_MAX_CRSE_ID)||V_SEQ1;
    EXECUTE IMMEDIATE V_CRSE_SESSION_SEQ_STR||TO_CHAR(V_MAX_CRSE_SESSION_ID)||V_SEQ1;
    EXECUTE IMMEDIATE V_CRSE_YEAR_SEQ_STR||TO_CHAR(V_MAX_CRSE_YEAR_ID)||V_SEQ1;

END;