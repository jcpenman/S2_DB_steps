COMPUTE STATISTICS;


CREATE INDEX S1_CRST ON CRSE_TERM
(CRSE_YEAR_ID)
LOGGING
TABLESPACE STEPS_INDEX
COMPUTE STATISTICS;


CREATE OR REPLACE TRIGGER CRST_UD
   AFTER INSERT OR DELETE OR UPDATE OF crse_year_id,
                                       term_no,
                                       days,
                                       start_date,
                                       end_date
   ON CRSE_TERM
   FOR EACH ROW
DECLARE
   p_crse_year_id     crse_term_change.crse_year_id%TYPE      := NULL;
BEGIN
   IF INSERTING
   THEN
      p_crse_year_id := TO_CHAR (:NEW.crse_year_id);
   ELSIF UPDATING
   THEN
     p_crse_year_id := TO_CHAR (:OLD.crse_year_id);
   ELSIF DELETING
   THEN
      p_crse_year_id := TO_CHAR (:OLD.crse_year_id);
   END IF;

   
   
      INSERT INTO crse_term_change
                  (crse_year_id, change_date
                  )
           VALUES (p_crse_year_id, SYSDATE
                  );
   INSERT INTO SLCADMIN.RESET_COURSE RC (RC.CRSE_ID, RC.SESSION_CODE)
      SELECT DISTINCT CS.CRSE_ID, CS.SESSION_CODE
        FROM CRSE_SESSION CS, CRSE_YEAR CY
       WHERE     CY.CRSE_YEAR_ID = NVL (:NEW.CRSE_YEAR_ID, :OLD.CRSE_YEAR_ID)
             AND CY.CRSE_SESSION_ID = CS.CRSE_SESSION_ID
             AND NOT EXISTS
                        (SELECT 1
                           FROM SLCADMIN.RESET_COURSE RC1
                          WHERE     RC1.CRSE_ID = CS.CRSE_ID
                                AND RC1.SESSION_CODE = CS.SESSION_CODE);
   
END crst_ud;



