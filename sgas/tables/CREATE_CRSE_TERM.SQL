CREATE TABLE CRSE_TERM
(
  CRSE_YEAR_ID  NUMBER(9) CONSTRAINT NN_CRST_CRSE_YEAR_ID NOT NULL,
  TERM_NO       NUMBER(2) CONSTRAINT NN_CRST_TERM_NO NOT NULL,
  DAYS          NUMBER(3) CONSTRAINT NN_CRST_DAYS NOT NULL,
  START_DATE    DATE CONSTRAINT NN_CRST_START_DATE NOT NULL,
  END_DATE      DATE CONSTRAINT NN_CRST_END_DATE NOT NULL
)
TABLESPACE STEPS_DATA
LOGGING ;

INSERT INTO CRSE_TERM (CRSE_YEAR_ID, TERM_NO, DAYS, START_DATE, END_DATE)
SELECT CRSE_YEAR_ID, TERM_NO, DAYS, START_DATE, END_DATE
FROM CRSE_TERM@GRASS;

BEGIN
