    ,Method_Opt        => 'FOR ALL COLUMNS SIZE AUTO'
    ,Degree            => 4
    ,Cascade           => TRUE
    ,No_Invalidate  => FALSE);
END;

CREATE UNIQUE INDEX P_CRST ON CRSE_TERM
(CRSE_YEAR_ID, TERM_NO)
LOGGING
