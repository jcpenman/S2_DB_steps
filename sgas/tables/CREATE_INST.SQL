CREATE TABLE INST
(
  INST_CODE           VARCHAR2(5 BYTE) CONSTRAINT NN_IN_INST_CODE NOT NULL,
  INST_TYPE_ID        NUMBER(4),
  INST_NAME           VARCHAR2(50 BYTE) CONSTRAINT NN_IN_INST_NAME NOT NULL,
  NOMINATED_IND       VARCHAR2(1 BYTE)          DEFAULT 'N' CONSTRAINT NN_IN_NOMINATED_IND NOT NULL,
  SEMESTER            VARCHAR2(1 BYTE)          DEFAULT 'N',
  CATEGORY            VARCHAR2(2 BYTE)          DEFAULT 'NO',
  REGION_CODE         NUMBER(4),
  COLLEGE_TYPE        NUMBER(4),
  PAYMENT_METHOD      VARCHAR2(1 BYTE)          DEFAULT 'C',
  LOCATION_IND        VARCHAR2(1 BYTE),
  NOM_NAME            VARCHAR2(25 BYTE),
  HOUSE_NO_OR_NAME    VARCHAR2(32 BYTE),
  ADDR_L1             VARCHAR2(65 BYTE),
  ADDR_L2             VARCHAR2(65 BYTE),
  ADDR_L3             VARCHAR2(32 BYTE),
  ADDR_L4             VARCHAR2(32 BYTE),
  POST_CODE           VARCHAR2(8 BYTE),
  ADDR_EASTING        VARCHAR2(6 BYTE),
  ADDR_NORTHING       VARCHAR2(6 BYTE),
  BANK_NAME           VARCHAR2(25 BYTE),
  BANK_SORT_CODE      VARCHAR2(6 BYTE),
  ACCOUNT_NO          VARCHAR2(10 BYTE),
  BANK_HOUSE_NO_NAME  VARCHAR2(32 BYTE),
  BANK_ADDR_L1        VARCHAR2(65 BYTE),
  BANK_ADDR_L2        VARCHAR2(65 BYTE),
  BANK_ADDR_L3        VARCHAR2(32 BYTE),
  BANK_ADDR_L4        VARCHAR2(32 BYTE),
  BANK_POST_CODE      VARCHAR2(8 BYTE),
  TELE_NO             VARCHAR2(15 BYTE),
  FINANCE_CONTACT     VARCHAR2(25 BYTE),
  FINANCE_OFFICE      VARCHAR2(25 BYTE),
  PRINCIPAL           VARCHAR2(25 BYTE),
  FORMS_OFFICE        VARCHAR2(25 BYTE),
  SKELETON            VARCHAR2(1 BYTE)          DEFAULT 'Y' CONSTRAINT NN_IN_SKELETON NOT NULL,
  MAILSORT            NUMBER(5),
  LABELS              VARCHAR2(1 BYTE)          DEFAULT 'Y',
  LETTER              VARCHAR2(1 BYTE)          DEFAULT 'Y',
  BREAK               VARCHAR2(1 BYTE)          DEFAULT 'Y',
  INSTAL_NO           VARCHAR2(2 BYTE),
  INSTAL_INFO         VARCHAR2(255 BYTE),
  PAMS                VARCHAR2(1 BYTE)          DEFAULT 'N',
  CAM_DEBT            VARCHAR2(1 BYTE)          DEFAULT 'Y',
  LAST_STATUS_REP     DATE,
  CSV_ID              NUMBER(6)                 DEFAULT 0,
  LIS_ID              NUMBER(6)                 DEFAULT 0,
  NON_PUBLIC_FUND     VARCHAR2(1 BYTE)          DEFAULT 'N',
  HEI_INST_CODE       VARCHAR2(4 BYTE),
  BACS_FIRST          VARCHAR2(1 BYTE)          DEFAULT 'N'                   NOT NULL
)
TABLESPACE STEPS_DATA
LOGGING ;


INSERT INTO INST (INST_CODE, INST_TYPE_ID, INST_NAME, NOMINATED_IND, SEMESTER, CATEGORY, REGION_CODE, COLLEGE_TYPE, PAYMENT_METHOD, LOCATION_IND, NOM_NAME, HOUSE_NO_OR_NAME, ADDR_L1, ADDR_L2, ADDR_L3, ADDR_L4, POST_CODE, ADDR_EASTING, ADDR_NORTHING, BANK_NAME, BANK_SORT_CODE, ACCOUNT_NO, BANK_HOUSE_NO_NAME, BANK_ADDR_L1, BANK_ADDR_L2, BANK_ADDR_L3, BANK_ADDR_L4, BANK_POST_CODE, TELE_NO, FINANCE_CONTACT, FINANCE_OFFICE, PRINCIPAL, FORMS_OFFICE, SKELETON, MAILSORT, LABELS, LETTER, BREAK, INSTAL_NO, INSTAL_INFO, PAMS, CAM_DEBT, LAST_STATUS_REP, CSV_ID, LIS_ID, NON_PUBLIC_FUND, HEI_INST_CODE, BACS_FIRST)
SELECT INST_CODE, INST_TYPE_ID, INST_NAME, NOMINATED_IND, SEMESTER, CATEGORY, REGION_CODE, COLLEGE_TYPE, PAYMENT_METHOD, LOCATION_IND, NOM_NAME, HOUSE_NO_OR_NAME, ADDR_L1, ADDR_L2, ADDR_L3, ADDR_L4, POST_CODE, ADDR_EASTING, ADDR_NORTHING, BANK_NAME, BANK_SORT_CODE, ACCOUNT_NO, BANK_HOUSE_NO_NAME, BANK_ADDR_L1, BANK_ADDR_L2, BANK_ADDR_L3, BANK_ADDR_L4, BANK_POST_CODE, TELE_NO, FINANCE_CONTACT, FINANCE_OFFICE, PRINCIPAL, FORMS_OFFICE, SKELETON, MAILSORT, LABELS, LETTER, BREAK, INSTAL_NO, INSTAL_INFO, PAMS, CAM_DEBT, LAST_STATUS_REP, CSV_ID, LIS_ID, NON_PUBLIC_FUND, HEI_INST_CODE, BACS_FIRST
FROM INST@GRASS;

BEGIN
  SYS.DBMS_STATS.GATHER_TABLE_STATS (
     OwnName           => 'SGAS'
    ,TabName           => 'INST'
    ,Estimate_Percent  => 10
    ,Method_Opt        => 'FOR ALL COLUMNS SIZE AUTO'
    ,Degree            => 4
    ,Cascade           => TRUE
    ,No_Invalidate  => FALSE);
END;

