CREATE TABLE CAMPUS
(
  CAMPUS_ID           NUMBER(9) CONSTRAINT NN_CAM_CAMPUS_ID NOT NULL,
  INST_CODE           VARCHAR2(5 BYTE) CONSTRAINT NN_CAM_INST_CODE NOT NULL,
  CAMPUS_CODE         VARCHAR2(1 BYTE) CONSTRAINT NN_CAM_CAMPUS_CODE NOT NULL,
  NAME                VARCHAR2(50 BYTE) CONSTRAINT NN_CAM_NAME NOT NULL,
  HOUSE_NO_NAME       VARCHAR2(32 BYTE),
  ADDR_L1             VARCHAR2(65 BYTE),
  ADDR_L2             VARCHAR2(65 BYTE),
  ADDR_L3             VARCHAR2(32 BYTE),
  ADDR_L4             VARCHAR2(32 BYTE),
  POSTCODE            VARCHAR2(8 BYTE),
  ADDR_EASTING        VARCHAR2(6 BYTE),
  ADDR_NORTHING       VARCHAR2(6 BYTE),
  PAYMENT_METHOD      VARCHAR2(1 BYTE)          DEFAULT 'C',
  BANK_NAME           VARCHAR2(25 BYTE),
  BANK_SORT_CODE      VARCHAR2(6 BYTE),
  ACCOUNT_NO          VARCHAR2(10 BYTE),
  BANK_HOUSE_NO_NAME  VARCHAR2(32 BYTE),
  BANK_ADDR_L1        VARCHAR2(65 BYTE),
  BANK_ADDR_L2        VARCHAR2(65 BYTE),
  BANK_ADDR_L3        VARCHAR2(32 BYTE),
  BANK_ADDR_L4        VARCHAR2(32 BYTE),
  BANK_POST_CODE      VARCHAR2(8 BYTE),
  CAMPUS_DEBT         NUMBER(9,2)               DEFAULT 0,
  CSV_ID              NUMBER(6)                 DEFAULT 0,
  FEE_LOAN_DEBT       NUMBER(9,2)               DEFAULT 0
)
TABLESPACE STEPS_DATA
LOGGING ;

INSERT INTO CAMPUS (CAMPUS_ID, INST_CODE, CAMPUS_CODE, NAME, HOUSE_NO_NAME, ADDR_L1, ADDR_L2, ADDR_L3, ADDR_L4, POSTCODE, ADDR_EASTING, ADDR_NORTHING, PAYMENT_METHOD, BANK_NAME, BANK_SORT_CODE, ACCOUNT_NO, BANK_HOUSE_NO_NAME, BANK_ADDR_L1, BANK_ADDR_L2, BANK_ADDR_L3, BANK_ADDR_L4, BANK_POST_CODE, CAMPUS_DEBT, CSV_ID, FEE_LOAN_DEBT)
SELECT CAMPUS_ID, INST_CODE, CAMPUS_CODE, NAME, HOUSE_NO_NAME, ADDR_L1, ADDR_L2, ADDR_L3, ADDR_L4, POSTCODE, ADDR_EASTING, ADDR_NORTHING, PAYMENT_METHOD, BANK_NAME, BANK_SORT_CODE, ACCOUNT_NO, BANK_HOUSE_NO_NAME, BANK_ADDR_L1, BANK_ADDR_L2, BANK_ADDR_L3, BANK_ADDR_L4, BANK_POST_CODE, CAMPUS_DEBT, CSV_ID, FEE_LOAN_DEBT
FROM CAMPUS@GRASS;

BEGIN
  SYS.DBMS_STATS.GATHER_TABLE_STATS (
     OwnName           => 'SGAS'
    ,TabName           => 'CAMPUS'
    ,Estimate_Percent  => 10
    ,Method_Opt        => 'FOR ALL COLUMNS SIZE AUTO'
    ,Degree            => 4
    ,Cascade           => TRUE
    ,No_Invalidate  => FALSE);
END;

CREATE UNIQUE INDEX P_CAM ON CAMPUS
(CAMPUS_ID)
LOGGING
TABLESPACE STEPS_INDEX
COMPUTE STATISTICS;


CREATE UNIQUE INDEX U1_CAM ON CAMPUS
(INST_CODE, CAMPUS_CODE)
LOGGING
TABLESPACE STEPS_INDEX
COMPUTE STATISTICS;

