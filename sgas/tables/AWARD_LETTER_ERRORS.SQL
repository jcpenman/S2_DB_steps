--
-- PAY_ERRORS_ID_seq  (Sequence) 
--
CREATE SEQUENCE SGAS.AWARD_ERRORS_ID_SEQ
  START WITH 1
  MAXVALUE 9999999999
  MINVALUE 1
  CYCLE
  NOCACHE
  NOORDER
/

CREATE OR REPLACE TRIGGER SGAS.TRIG_AWARD_ERRORS_ID_SEQ 
   BEFORE INSERT
   ON SGAS.AWARD_LETTER_ERRORS
   FOR EACH ROW
BEGIN
   SELECT AWARD_ERRORS_ID_SEQ.NEXTVAL
     INTO :NEW.AWARD_ERROR_ID
     FROM DUAL;
END;
