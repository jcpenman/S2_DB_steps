(
   CUSTOMER_RECORD_STATUS_CODE      NUMBER (2) NOT NULL,
   CUSTOMER_RECORD_STATUS_LABEL     VARCHAR2 (15 BYTE),
   CUSTOMER_RECORD_STATUS_DESCRIP   VARCHAR2 (100 BYTE)
)
TABLESPACE USERS
PCTUSED 0
PCTFREE 10
INITRANS 1
MAXTRANS 255
STORAGE (INITIAL 64 K
         MINEXTENTS 1
         MAXEXTENTS UNLIMITED
         PCTINCREASE 0
         BUFFER_POOL DEFAULT)
LOGGING
NOCOMPRESS
NOCACHE
NOPARALLEL
MONITORING;

COMMENT ON TABLE SGAS.SLC_CUSTOMER_RECORD_STATUSES IS
	'Reference table describing the various statuses related to the SLC Customer Record File functionality';

COMMENT ON COLUMN SGAS.SLC_CUSTOMER_RECORD_STATUSES.CUSTOMER_RECORD_STATUS_CODE IS
   'Status code for a customer record';

COMMENT ON COLUMN SGAS.SLC_CUSTOMER_RECORD_STATUSES.CUSTOMER_RECORD_STATUS_LABEL IS
   'Status label';

COMMENT ON COLUMN SGAS.SLC_CUSTOMER_RECORD_STATUSES.CUSTOMER_RECORD_STATUS_DESCRIP IS
   'Status code description';


