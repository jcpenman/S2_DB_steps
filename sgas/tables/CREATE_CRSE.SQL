CREATE OR REPLACE TRIGGER CRSE_IUD
   AFTER INSERT OR DELETE OR UPDATE OF CRSE_NAME, MAINT_CAMPUS, HEI_CRSE_CODE
   ON CRSE
   FOR EACH ROW
BEGIN
   INSERT INTO SLCADMIN.RESET_COURSE RC (RC.CRSE_ID, RC.SESSION_CODE)
      SELECT NVL (:NEW.CRSE_ID, :OLD.CRSE_ID), NULL
        FROM SLCADMIN.RESET_COURSE RC1
       WHERE     RC1.CRSE_ID = NVL (:NEW.CRSE_ID, :OLD.CRSE_ID)
             AND RC1.SESSION_CODE IS NULL;
END;

