CREATE TABLE CRSE
(
  CRSE_ID         NUMBER(9) CONSTRAINT NN_CRS_CRSE_ID NOT NULL,
  INST_CODE       VARCHAR2(5 BYTE) CONSTRAINT NN_CRS_INST_CODE NOT NULL,
  INST_NAME       VARCHAR2(50 BYTE) CONSTRAINT NN_CRS_INST_NAME NOT NULL,
  CRSE_CODE       VARCHAR2(4 BYTE) CONSTRAINT NN_CRS_CRSE_CODE NOT NULL,
  CRSE_NAME       VARCHAR2(50 BYTE) CONSTRAINT NN_CRS_CRSE_NAME NOT NULL,
  FEES_CAMPUS     NUMBER(9),
  MAINT_CAMPUS    NUMBER(9),
  QUAL_TYPE       VARCHAR2(10 BYTE),
  SCHEME_TYPE     VARCHAR2(1 BYTE)              DEFAULT 'U',
  APPROVED        VARCHAR2(1 BYTE)              DEFAULT 'Y',
  DEPARTMENT      VARCHAR2(25 BYTE),
  SUBJECT_CAT_ID  NUMBER(4),
  SUPERVISOR      VARCHAR2(25 BYTE),
  PAMS_COURSE     VARCHAR2(1 BYTE)              DEFAULT 'N' CONSTRAINT NN_CRS_PAMS_COURSE NOT NULL,
  SKELETON        VARCHAR2(1 BYTE)              DEFAULT 'Y' CONSTRAINT NN_CRS_SKELETON NOT NULL,
  HEI_CRSE_CODE   VARCHAR2(6 BYTE),
  GE_LIABLE       VARCHAR2(1 BYTE)
)
TABLESPACE STEPS_DATA
LOGGING ;

INSERT INTO CRSE (CRSE_ID, INST_CODE, INST_NAME, CRSE_CODE, CRSE_NAME, FEES_CAMPUS, MAINT_CAMPUS, QUAL_TYPE, SCHEME_TYPE, APPROVED, DEPARTMENT, SUBJECT_CAT_ID, SUPERVISOR, PAMS_COURSE, SKELETON, HEI_CRSE_CODE, GE_LIABLE)
SELECT CRSE_ID, INST_CODE, INST_NAME, CRSE_CODE, CRSE_NAME, FEES_CAMPUS, MAINT_CAMPUS, QUAL_TYPE, SCHEME_TYPE, APPROVED, DEPARTMENT, SUBJECT_CAT_ID, SUPERVISOR, PAMS_COURSE, SKELETON, HEI_CRSE_CODE, GE_LIABLE
FROM CRSE@GRASS;

