DROP MATERIALIZED VIEW CRSE_SESSION;
CREATE MATERIALIZED VIEW CRSE_SESSION (CRSE_SESSION_ID,CRSE_ID,SESSION_CODE,MAX_DURATION,PSAS_LOAN,PSAS_CAT)
TABLESPACE SAAS_DATA
LOGGING
BUILD IMMEDIATE
USING INDEX
            TABLESPACE SAAS_DATA
REFRESH FAST
START WITH SYSDATE
WITH ROWID
AS 
/* Formatted on 15/11/2018 13:38:21 (QP5 v5.256.13226.35538) */
SELECT crse_session_id,
       crse_id,
       session_code,
       max_duration,
       psas_loan,
       psas_cat
  FROM crse_session@steps.world;


COMMENT ON MATERIALIZED VIEW CRSE_SESSION IS 'snapshot table for snapshot SGAS.CRSE_SESSION';

CREATE UNIQUE INDEX U1_CRSE_SESS ON CRSE_SESSION
(CRSE_ID, CRSE_SESSION_ID, SESSION_CODE)
LOGGING
TABLESPACE SAAS_DATA;

CREATE INDEX U2_CRSE_SESS ON CRSE_SESSION
(CRSE_ID, SESSION_CODE)
LOGGING
TABLESPACE SAAS_DATA;

