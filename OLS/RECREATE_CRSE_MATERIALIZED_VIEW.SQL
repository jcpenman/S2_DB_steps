DROP MATERIALIZED VIEW CRSE;
CREATE MATERIALIZED VIEW CRSE (CRSE_ID,INST_CODE,CRSE_CODE,CRSE_NAME,SCHEME_TYPE,QUAL_TYPE,PAMS_COURSE)
TABLESPACE SAAS_DATA
LOGGING
BUILD IMMEDIATE
USING INDEX
            TABLESPACE SAAS_DATA
REFRESH FAST
START WITH SYSDATE
WITH ROWID
AS 
/* Formatted on 15/11/2018 13:38:52 (QP5 v5.256.13226.35538) */
SELECT crse_id,
       inst_code,
       crse_code,
       crse_name,
       scheme_type,
       qual_type,
       pams_course
  FROM crse@steps.world
 WHERE crse_code != 'PART' AND crse_name NOT LIKE 'Z%';


COMMENT ON MATERIALIZED VIEW CRSE IS 'snapshot table for snapshot SGAS.CRSE';

-- Note: Index I_SNAP$_CRSE will be created automatically 
--       by Oracle with the associated materialized view.

CREATE UNIQUE INDEX U1_CRSE ON CRSE
(CRSE_ID, INST_CODE)
LOGGING
TABLESPACE SAAS_DATA;

CREATE INDEX U2_CRSE ON CRSE
(INST_CODE, SCHEME_TYPE)
LOGGING
TABLESPACE SAAS_DATA;

CREATE INDEX U3_CRSE ON CRSE
(CRSE_ID, INST_CODE, SCHEME_TYPE)
LOGGING
TABLESPACE SAAS_DATA;
