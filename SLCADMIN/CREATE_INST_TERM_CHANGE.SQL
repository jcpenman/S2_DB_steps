CREATE SEQUENCE SLCADMIN.INST_TERM_CHANGE_SEQ START WITH 1
                                              MAXVALUE 9999999999
                                              MINVALUE 1
                                              NOCYCLE
                                              NOCACHE
                                              NOORDER;

CREATE TABLE SLCADMIN.INST_TERM_CHANGE
(
   HEI_INST_CODE         VARCHAR2 (4 BYTE),
   INST_CODE             VARCHAR2 (5 BYTE) NOT NULL,
   SESSION_CODE          NUMBER (4) NOT NULL,
   TERM_NO               NUMBER (2) NOT NULL,
   NEW_START_DATE            DATE ,
   NEW_END_DATE              DATE ,
   NEW_DAYS                  NUMBER (3) ,
   OLD_START_DATE        DATE ,
   OLD_END_DATE          DATE ,
   OLD_DAYS              NUMBER (3) ,
   CHANGE_TYPE           VARCHAR2 (1 BYTE),
   CREATED_DATE          DATE,
   CREATED_BY            VARCHAR2 (50 BYTE),
   STATUS                VARCHAR2 (20 BYTE),
   STATUS_BY             VARCHAR2 (50 BYTE),
   INST_TERM_CHANGE_ID   NUMBER (10) NOT NULL,
   LAST_MODIFIED_DATE    DATE DEFAULT SYSDATE
)
TABLESPACE SLC_DATA
RESULT_CACHE (MODE DEFAULT)
LOGGING;

CREATE UNIQUE INDEX P_ITC
   ON INST_TERM_CHANGE (INST_TERM_CHANGE_ID)
   LOGGING
   TABLESPACE SLC_DATA;

CREATE INDEX ITC_IND1
   ON INST_TERM_CHANGE (INST_CODE, SESSION_CODE, TERM_NO)
   LOGGING
   TABLESPACE SLC_DATA;

CREATE INDEX ITC_IND2
   ON INST_TERM_CHANGE (HEI_INST_CODE, SESSION_CODE, TERM_NO)
   LOGGING
   TABLESPACE SLC_DATA;



ALTER TABLE INST_TERM_CHANGE ADD (
  CONSTRAINT P_ITC
  PRIMARY KEY
  (INST_TERM_CHANGE_ID)
  USING INDEX P_ITC
  ENABLE VALIDATE);

CREATE OR REPLACE TRIGGER INST_TERM_CHANGE_ID_SEQ
   BEFORE INSERT
   ON INST_TERM_CHANGE
   FOR EACH ROW
BEGIN
   SELECT INST_TERM_CHANGE_SEQ.NEXTVAL INTO :NEW.INST_TERM_CHANGE_ID FROM DUAL;
END;

GRANT SELECT ON INST_TERM_CHANGE TO SGAS_EUL;

GRANT DELETE,
      INSERT,
      SELECT,
      UPDATE
   ON INST_TERM_CHANGE
   TO SGAS;