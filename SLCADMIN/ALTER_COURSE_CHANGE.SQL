ALTER TABLE SLCADMIN.COURSE_CHANGE
 ADD (COURSE_CHANGE_ID  NUMBER(10));
 
 CREATE SEQUENCE COURSE_CHANGE_SEQ
  START WITH 1
  MAXVALUE 9999999999
  MINVALUE 1
  NOCYCLE
  NOCACHE
  NOORDER;
  
  
DECLARE
   CURSOR C_COURSE_CHANGE
   IS
      SELECT *
        FROM COURSE_CHANGE CC
       WHERE CC.COURSE_CHANGE_ID IS NULL
      FOR UPDATE;

   V_COURSE_CHANGE      COURSE_CHANGE%ROWTYPE;
   V_COURSE_CHANGE_ID   NUMBER;
BEGIN
   OPEN C_COURSE_CHANGE;

   LOOP
      FETCH C_COURSE_CHANGE INTO V_COURSE_CHANGE;

      EXIT WHEN C_COURSE_CHANGE%NOTFOUND;

      SELECT COURSE_CHANGE_SEQ.NEXTVAL INTO V_COURSE_CHANGE_ID FROM DUAL;

      UPDATE COURSE_CHANGE CC
         SET CC.COURSE_CHANGE_ID = V_COURSE_CHANGE_ID
       WHERE CURRENT OF C_COURSE_CHANGE;
   END LOOP;

   CLOSE C_COURSE_CHANGE;
END;

ALTER TABLE SLCADMIN.COURSE_CHANGE
 MODIFY (COURSE_CHANGE_ID  NUMBER(10) NOT NULL);
 
 CREATE OR REPLACE TRIGGER COURSE_CHANGE_ID_SEQ
   BEFORE INSERT
   ON COURSE_CHANGE   FOR EACH ROW
BEGIN
   SELECT COURSE_CHANGE_SEQ.NEXTVAL
     INTO :NEW.COURSE_CHANGE_ID
     FROM DUAL;
END;


CREATE UNIQUE INDEX SLCADMIN.COUC_IND4 ON SLCADMIN.COURSE_CHANGE
(COURSE_CHANGE_ID)
COMPUTE STATISTICS;

