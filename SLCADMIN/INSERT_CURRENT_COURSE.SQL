Insert into CURRENT_COURSE
   (HEI_INST_CODE, INST_CODE, CAMPUS, HEI_CRSE_CODE, CRSE_CODE, 
    SCOTTISH, HEI_CRSE_NAME, SESSION_CODE, DURATION)
SELECT  I.HEI_INST_CODE,
                I.INST_CODE,
                C.MAINT_CAMPUS CAMPUS,
                HC.SLC_COURSE_CODE HEI_CRSE_CODE,
                C.CRSE_CODE,
                'Y' SCOTTISH,
                HC.HEI_CRSE_NAME,
                MAX(CS.SESSION_CODE) SESSION_CODE,
                CS.MAX_DURATION DURATION
  FROM SGAS.INST@GRASS I, SGAS.CRSE@GRASS C, SGAS.HEI_INST@GRASS HI, SGAS.HEI_CRSE@GRASS HC, SGAS.CRSE_SESSION@GRASS CS
 WHERE     I.INST_CODE = C.INST_CODE
       AND I.HEI_INST_CODE = HI.HEI_INST_CODE
       AND (C.HEI_CRSE_CODE = to_char(HC.SLC_COURSE_CODE) or
          C.HEI_CRSE_CODE =  HC.HEI_CRSE_CODE )
        AND C.CRSE_ID = CS.CRSE_ID
       AND CS.SESSION_CODE IN (2018,2017)
                GROUP BY I.HEI_INST_CODE,
                I.INST_CODE,
                C.MAINT_CAMPUS ,
                HC.SLC_COURSE_CODE,
                C.CRSE_CODE,
                HC.HEI_CRSE_NAME,
                CS.MAX_DURATION;
COMMIT;
