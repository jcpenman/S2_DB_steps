ALTER TABLE SLCADMIN.COLLEGE_CHANGE
 ADD (COLLEGE_CHANGE_ID  NUMBER(10));
 
 CREATE SEQUENCE COLLEGE_CHANGE_SEQ
  START WITH 1
  MAXVALUE 9999999999
  MINVALUE 1
  NOCYCLE
  NOCACHE
  NOORDER;
  
  
DECLARE
   CURSOR C_COLLEGE_CHANGE
   IS
      SELECT *
        FROM COLLEGE_CHANGE CC
       WHERE CC.COLLEGE_CHANGE_ID IS NULL
      FOR UPDATE;

   V_COLLEGE_CHANGE      COLLEGE_CHANGE%ROWTYPE;
   V_COLLEGE_CHANGE_ID   NUMBER;
BEGIN
   OPEN C_COLLEGE_CHANGE;

   LOOP
      FETCH C_COLLEGE_CHANGE INTO V_COLLEGE_CHANGE;

      EXIT WHEN C_COLLEGE_CHANGE%NOTFOUND;

      SELECT COLLEGE_CHANGE_SEQ.NEXTVAL INTO V_COLLEGE_CHANGE_ID FROM DUAL;

      UPDATE COLLEGE_CHANGE CC
         SET CC.COLLEGE_CHANGE_ID = V_COLLEGE_CHANGE_ID
       WHERE CURRENT OF C_COLLEGE_CHANGE;
   END LOOP;

   CLOSE C_COLLEGE_CHANGE;
END;

ALTER TABLE SLCADMIN.COLLEGE_CHANGE
 MODIFY (COLLEGE_CHANGE_ID  NUMBER(10) NOT NULL);
 
 CREATE OR REPLACE TRIGGER COLLEGE_CHANGE_ID_SEQ
   BEFORE INSERT
   ON COLLEGE_CHANGE   FOR EACH ROW
BEGIN
   SELECT COLLEGE_CHANGE_SEQ.NEXTVAL
     INTO :NEW.COLLEGE_CHANGE_ID
     FROM DUAL;
END;


CREATE UNIQUE INDEX SLCADMIN.COLC_IND4 ON SLCADMIN.COLLEGE_CHANGE
(COLLEGE_CHANGE_ID)
COMPUTE STATISTICS;

BEGIN
  SYS.DBMS_STATS.GATHER_TABLE_STATS (
     OwnName           => 'SLCADMIN'
    ,TabName           => 'COLLEGE_CHANGE'
    ,Estimate_Percent  => 10
    ,Method_Opt        => 'FOR ALL COLUMNS SIZE AUTO'
    ,Degree            => 4
    ,Cascade           => TRUE
    ,No_Invalidate  => FALSE);
END;

